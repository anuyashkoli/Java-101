public class Conditionals {
    public static void main(String[] args) {
        int age = 15;
        if (age == 15) {
            age += 5;
            System.out.println("After 5 years after 15 years old:  " + age);
        }
        if (age == 20) {
            age += 10;
            System.out.println("Age after 10 years from 20 years old: " + age);
        }
        if (age == 30) System.out.println("Tees Maar Khan");
    }
}